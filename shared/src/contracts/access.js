export function getPermissionsForProfile(profileType) {
  return [...(ROLE_PERMISSION_MATRIX[profileType] || [])];
}

export function hasPermission(profileType, permission) {
  return getPermissionsForProfile(profileType).includes(permission);
}

export function hasAnyPermission(profileType, permissions = []) {
  if (!Array.isArray(permissions) || permissions.length === 0) {
    return true;
  }

  return permissions.some((permission) => hasPermission(profileType, permission));
}

export function isAdministrativeProfileType(profileType) {
  return ADMINISTRATIVE_PROFILE_TYPES.includes(profileType);
}

export function canAccessPage(profileType, pageName) {
  const requiredPermissions = PAGE_PERMISSION_MATRIX[pageName];
  if (!requiredPermissions) return false;
  if (requiredPermissions.length === 0) return true;
  return hasAnyPermission(profileType, requiredPermissions);
}

